SELECT EMPLOYEE_ID 
FROM EMPLOYEES 

MINUS
(
	SELECT MANAGER_ID
	FROM EMPLOYEES
);


/*---B---*/
SELECT LAST_NAME,EMPLOYEE_ID
FROM EMPLOYEES 
WHERE EMPLOYEE_ID= ANY
(
	SELECT E1.EMPLOYEE_ID
	FROM EMPLOYEES E1
	MINUS
	(
		SELECT E2.MANAGER_ID
		FROM EMPLOYEES E2
	)

)

ORDER BY EMPLOYEE_ID ASC;


